CREATE TABLE
    THIPPERS.LOTTI_PRNT
    (
        ID_AZIENDA CHARACTER(3) NOT NULL,
        ID_ARTICOLO CHARACTER(25) NOT NULL,
        ID_LOTTO CHARACTER(15) NOT NULL,
        ID_COMMESSA CHARACTER(10) NOT NULL,
        QUANTITA DECIMAL(19,2),
        DATA DATE,
        R_UTENTE_CRZ CHARACTER(20) NOT NULL,
        R_UTENTE_AGG CHARACTER(20) NOT NULL,
        TIMESTAMP_CRZ DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
        TIMESTAMP_AGG DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
        ESEGUITA CHARACTER(1),
        DSC_COMMESSA CHARACTER(10),
        TIPO_PRENOTAZIONE CHARACTER(1) DEFAULT 'M' NOT NULL,
        PRIMARY KEY (ID_AZIENDA, ID_ARTICOLO, ID_LOTTO, ID_COMMESSA),
        CONSTRAINT LOTPRNT2LOTTI_X FOREIGN KEY (ID_AZIENDA, ID_ARTICOLO, ID_LOTTO) REFERENCES
        THIPPERS.LOTTI_X (ID_AZIENDA, ID_ARTICOLO, ID_LOTTO) ON
    DELETE
        CASCADE
    );
