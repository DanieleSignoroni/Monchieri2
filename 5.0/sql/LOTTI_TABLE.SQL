CREATE TABLE
    THIPPERS.LOTTI_X
    (
        ID_AZIENDA CHARACTER(3) NOT NULL,
        ID_ARTICOLO CHARACTER(25) NOT NULL,
        ID_LOTTO CHARACTER(15) NOT NULL,
        COD_LOTTO_CLI CHARACTER(25),
        ANNOTAZIONI VARCHAR(250),
        NUCLEARE CHARACTER(1),
        YSTATO CHARACTER(1),
        PRIMARY KEY (ID_AZIENDA, ID_ARTICOLO, ID_LOTTO),
        CONSTRAINT LOTTI_X2LOTTI FOREIGN KEY (ID_AZIENDA, ID_ARTICOLO, ID_LOTTO) REFERENCES
        THIP.LOTTI (ID_AZIENDA, ID_ARTICOLO, ID_LOTTO) ON
    DELETE
        CASCADE
    );
