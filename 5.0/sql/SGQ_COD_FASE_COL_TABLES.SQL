CREATE TABLE
    THIPPERS.SGQ_COD_FASE_COL
    (
        ID_AZIENDA CHARACTER(3) NOT NULL,
        CODICE SMALLINT NOT NULL,
        DESCRIZIONE VARCHAR(35) NOT NULL,
        DESCR_RIDOTTA CHARACTER(15) NOT NULL,
        STATO CHARACTER(1) NOT NULL,
        TIMESTAMP_CRZ DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
        TIMESTAMP_AGG DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
        R_UTENTE_CRZ CHARACTER(20) NOT NULL,
        R_UTENTE_AGG CHARACTER(20) NOT NULL,
        PRIMARY KEY (ID_AZIENDA, CODICE),
        CONSTRAINT CODFASC2AZIENDE FOREIGN KEY (ID_AZIENDA) REFERENCES THIP.AZIENDE_ESTESE
        (ID_AZIENDA)
    );


   CREATE TABLE THIPPERS.SGQ_COD_FASE_COL_L
(
    ID_AZIENDA CHARACTER(3) NOT NULL,
    CODICE SMALLINT NOT NULL,
    ID_LINGUA CHARACTER(2) NOT NULL,
    DESCRIZIONE VARCHAR(35) NOT NULL,
    DESCR_RIDOTTA CHARACTER(15) NOT NULL,
    PRIMARY KEY (ID_AZIENDA, CODICE, ID_LINGUA),
    CONSTRAINT CODFA2AZNESTE FOREIGN KEY (ID_AZIENDA) REFERENCES THIP.AZIENDE_ESTESE (ID_AZIENDA),
    CONSTRAINT CODFA2CODFASC1 FOREIGN KEY (ID_AZIENDA, CODICE) REFERENCES THIPPERS.SGQ_COD_FASE_COL (ID_AZIENDA, CODICE) 
    ON DELETE CASCADE,
    CONSTRAINT CODFA2LANG FOREIGN KEY (ID_LINGUA) REFERENCES THERA.LANGUAGE (ID)
    ON DELETE CASCADE
);
