CREATE TABLE
    THIPPERS.SGQ_COD_CAR_COL
    (
        ID_AZIENDA CHARACTER(3) NOT NULL,
        CODICE SMALLINT NOT NULL,
        DESCRIZIONE VARCHAR(35) NOT NULL,
        DESCR_RIDOTTA CHARACTER(15) NOT NULL,
        STATO CHARACTER(1) NOT NULL,
        TIMESTAMP_CRZ DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
        TIMESTAMP_AGG DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
        R_UTENTE_AGG CHARACTER(20) NOT NULL,
        R_UTENTE_CRZ CHARACTER(20) NOT NULL,
        FORMULA VARCHAR(90),
        PRIMARY KEY (ID_AZIENDA, CODICE),
        CONSTRAINT CODCARC2AZNESTE FOREIGN KEY (ID_AZIENDA) REFERENCES THIP.AZIENDE_ESTESE
        (ID_AZIENDA)
    );

	CREATE TABLE THIPPERS.SGQ_COD_CAR_COL_L(
    ID_AZIENDA CHARACTER(3) NOT NULL,
    CODICE SMALLINT NOT NULL,
    ID_LINGUA CHARACTER(2) NOT NULL,
    DESCRIZIONE VARCHAR(35) NOT NULL,
    DESCR_RIDOTTA CHARACTER(15) NOT NULL,
    PRIMARY KEY (ID_AZIENDA, CODICE, ID_LINGUA),
    CONSTRAINT CODCARL2AZNESTE FOREIGN KEY (ID_AZIENDA) REFERENCES THIP.AZIENDE_ESTESE (ID_AZIENDA),
    CONSTRAINT CODCARL2CODCARC1 FOREIGN KEY (ID_AZIENDA, CODICE) REFERENCES THIPPERS.SGQ_COD_CAR_COL (ID_AZIENDA, CODICE),
    CONSTRAINT CODCARL2LANG FOREIGN KEY (ID_LINGUA) REFERENCES THERA.LANGUAGE (ID)
);



